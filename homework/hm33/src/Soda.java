public class Soda {
  private String vitanta;
  private String color;
  public Soda(String vitanta, String color) {
    this.vitanta = vitanta;
    this.color = color;
  }
  public String getVitanta() {
    return vitanta;
  }
  public String getColor() {
    return color;
  }
  public void setColor(String color) {
    this.color = color;
  }
}
